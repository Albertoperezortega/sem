package com.napier.sem;

/**
 *
 * Represents a country
     */
    public class Country
    {
        /**
         * Country 3 letter code
         */
        public String code;

        /**
         * Country's name
         */
        public String name;

        /**
         * Name of continent
         */
        public String continent;

        /**
         * Name of the region
         */
        public String region;

        /**
         * Surface area of country
         */
        public float surface_area;

        /**
         * Independence year of the country
         */
        public int indep_year;

        /**
         * Population of the country
         */
        public int population;

        /**
         * Life expectency of the country's citizens
         */
        public float life_expectancy;

        /**
         * Gross national product of the country
         */
        public float gnp;

        /**
         * Old gross national product of the country
         */
        public float gnp_old;

        /**
         * Name of the country in their own language
         */
        public String local_name;

        /**
         * Government form: republic etc...
         */
        public String government_form;

        /**
         * Head of the country
         */
        public String head_of_state;

        /**
         * Capital of the country
         */
        public String capital;

        /**
         * Code2 of the country
         */
        public String code2;
    }
